Map::Map()
{
	dirt = TextureManager::LoadTexture("assets/dirt.png");
	grass = TextureManager::LoadTexture("assets/grass3.png");
	water = TextureManager::LoadTexture("assets/tree.png");
	LoadMap(lvl1);
	src.x = src.y = 0;
	src.h = des.h = 32;
	src.w = des.w = 32;
	des.x = des.y = 0;


}
Map::~Map()
{
	SDL_DestroyTexture(grass);
	SDL_DestroyTexture(water);
	SDL_DestroyTexture(dirt);

}
void Map::LoadMap(int arr[34][60])
{
	for (int row = 0; row < 34; row++)
	{
		for (int col = 0; col < 60; col++)
		{
			map[row][col] = arr[row][col];
		}
	}
}
void Map::DrawMap()
{
	int type = 0;
	for (int row = 0; row < 34; row++)
	{
		for (int col = 0; col < 60; col++)
		{
			type = map[row][col];
			des.x = col * 32;
			des.y = row * 32;
			switch (type)
			{
			case 0:
				TextureManager::Draw(water, src, des);
				break;
			case 1:
				TextureManager::Draw(dirt, src, des);
				break;
			case 2:
				TextureManager::Draw(grass, src, des);
				break;
			default:
				break;
			}

		}
	}
}